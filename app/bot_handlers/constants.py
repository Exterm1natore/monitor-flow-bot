    # --- base commands
    HELP = "help"
    REGISTER = "register"
    SIGN_OUT = "sign_out"
    STATUS = "status"
    STOP = "stop"
    START = "start"
    NOTIFY_ON = "notify_on"
    NOTIFY_OFF = "notify_off"

    # --- admin commands
    GET_DATA = "get_data"
    FIND_DATA = "find_data"
    ADD_NOTIFY_SUBSCRIBER = "add_notify_subscriber"
    DEL_NOTIFY_SUBSCRIBER = "del_notify_subscriber"
