@unique
class CallbackAction(Enum):
    VIEW_DB = "view_db"
    FIND_DB = "find_db"

    @staticmethod
    def is_valid(value: str) -> bool:
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–æ–ø—É—Å—Ç–∏–º—ã–º –¥–µ–π—Å—Ç–≤–∏–µ–º CallbackAction."""
